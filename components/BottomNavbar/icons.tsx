/**
 * Icon registry - maps icon names to components
 * This allows us to pass icon names as strings in config
 */
export const ICONS = {
  home: HomeIcon,
  trophy: TrophyIcon,
  service: ServiceIcon,
  info: InfoIcon,
} as const;

export type IconName = keyof typeof ICONS;

/**
 * Icon component props
 */
interface IconComponentProps {
  name: string;
  className?: string;
}

/**
 * Icon component that renders the appropriate icon based on name
 */
export function Icon({ name, className = '' }: IconComponentProps) {
  const IconComponent = ICONS[name as IconName];

  if (!IconComponent) {
    console.warn(`Icon "${name}" not found in registry`);
    return null;
  }

  return <IconComponent className={className} />;
}
